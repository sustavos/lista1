#include <iostream>

using namespace std;

int main(){
    cout << "Tamanho do inteiro = " << sizeof(int) << endl;
    cout << "Tamanho do short = " << sizeof(short) << endl;
    cout << "Tamanho do long = " << sizeof(long) << endl;
    cout << "Tamanho do char = " << sizeof(char) << endl;
    cout << "Tamanho do float = " << sizeof(float) << endl;
    cout << "Tamanho do double = " << sizeof(double) << endl;
    cout << "Tamanho do bool = " << sizeof(bool) << endl;
    return 0;
}
